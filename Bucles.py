#Archivo de ejemplo con bucles

#Declaracion de variables
#numero: guardo valores numericos para recorrer un bucle.
#cadena: va a almacenar una cadena de texto para diferentes usos.

# Recorrer un rango de valores numericos
for numero in range (1,11):
    print(numero)

# Recorrer una cadena de caracteres
    cadena = input()
    for letra in cadena:
        print(letra)

# Recorrer un rango de valores numericos, con un salto diferente de 1.
for numero in range (1,101,2):
    print(numero)

# Recorrer un rango de valores numericos hacia atras.
for numero in range (21,0,-2):
    print(numero)